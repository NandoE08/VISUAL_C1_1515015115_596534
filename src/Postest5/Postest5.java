    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        Object[] data = new Object[3];
        data[0] = nama.getText();
        data[1] = number.getText();
        data[2] = kota.getSelectedItem();
        
        model.addRow(data);

        update.setEnabled(true);
        delete.setEnabled(true);
        ulang.setEnabled(true);
        reset_table.setEnabled(true);
    }//GEN-LAST:event_saveActionPerformed

    private void reset_tableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reset_tableActionPerformed
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        nama.requestFocus();
    }//GEN-LAST:event_reset_tableActionPerformed

    private void namaCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_namaCaretUpdate
        // TODO add your handling code here:
        if(nama.getText().length() !=0 && number.getText().length() !=0){
            save.setEnabled(true);
        }
        else{
            save.setEnabled(false);
            update.setEnabled(false);
            delete.setEnabled(false);
            ulang.setEnabled(false);
        }
    }//GEN-LAST:event_namaCaretUpdate

