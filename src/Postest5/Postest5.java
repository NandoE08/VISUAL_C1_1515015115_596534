    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
        // TODO add your handling code here:
        int baris = tabel.getSelectedRow();
        
        String nama_edit = tabel.getValueAt(baris, 0).toString();
        String number_edit = tabel.getValueAt(baris, 1).toString();
        String kota_edit = tabel.getValueAt(baris, 2).toString();
        
        nama.setText(nama_edit);
        number.setText(number_edit);
        kota.setSelectedItem(kota_edit);
    }//GEN-LAST:event_saveMouseClicked

    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        // TODO add your handling code here:
        int baris = tabel.getSelectedRow();
        
        tabel.setValueAt(nama.getText(), baris, 0);
        tabel.setValueAt(number.getText(), baris, 1);
        tabel.setValueAt(kota.getSelectedItem() ,baris, 2);
    }//GEN-LAST:event_updateMouseClicked

